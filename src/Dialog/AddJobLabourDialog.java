    private void LoadFRUData(){
        ResultSet cd = Database.getData("SELECT * FROM tbl_labour");
        SelectLabour.removeAllItems();
        try {
            while(cd.next()){
                
                Vector v = new Vector();
                v.add(cd.getString(2));
                
                SelectLabour.addItem(cd.getString(2));
                 
            }
        } catch (SQLException ex) {
            Logger.getLogger(AddJobLabourDialog.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void AddBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_AddBtnMouseClicked

        if(FruValue.getText().isEmpty()){

            JOptionPane.showMessageDialog(rootPane, "Failed.");

        }else{
            
            ResultSet rs = Database.getData("SELECT * FROM tbl_job_labour WHERE job_id = '"+jobid+"' AND labour_name_1 = '"+SelectLabour.getSelectedItem().toString()+"'");
       
            try {
                if(rs.next()){

                    JOptionPane.showMessageDialog(rootPane, "Already Exists.");

                }else{
                    
                    System.out.println("INSERT INTO tbl_job_labour VALUES (default, '"+jobid+"', '0', '"+FruValue.getText()+"', '0', '"+SelectLabour.getSelectedItem().toString()+"', '', '"+pfm.format(new Date())+"' )");
                    
                    if(Database.saveData("INSERT INTO tbl_job_labour VALUES (default, '"+jobid+"', '0', '"+FruValue.getText()+"', '0', '"+SelectLabour.getSelectedItem().toString()+"', '', '"+pfm.format(new Date())+"' )")){
                        JOptionPane.showMessageDialog(rootPane, "Successfully labour added.");
                        System.out.println("Successfully labour added.");

                        OpenedJobCard.LoadJobItemData();
                        this.dispose();

                    }else{
                        JOptionPane.showMessageDialog(rootPane, "Failed.");
                        System.out.println("FAILED");
                    }
                }
            }catch(SQLException ed){
                ed.printStackTrace();
            }
            

        }

        
    }//GEN-LAST:event_AddBtnMouseClicked

