    private void SelectLabourActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SelectLabourActionPerformed
      
        if(SelectLabour.getSelectedItem() != null){
         
            ResultSet fru = Database.getData("SELECT * FROM tbl_labour WHERE labour_name = '"+SelectLabour.getSelectedItem().toString()+"' ");
            SelectLabour.addItem("Select Labour");
            try {
                if(fru.first()){

                    String frupoints = fru.getString("fru");

                    FruValue.setText(frupoints);

                }
            } catch (SQLException ex) {
               ex.printStackTrace();
            }
         
        }
       
    }//GEN-LAST:event_SelectLabourActionPerformed

