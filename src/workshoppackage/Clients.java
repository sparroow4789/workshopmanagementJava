    private void RegisterClientBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegisterClientBtnActionPerformed
        
        if(CustomerNameField.getText().isEmpty() || DateField.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Please fill the all area.");
        }else{
            
            String Birthday = SelectBirthMonth.getSelectedItem().toString()+" "+SelectBirthDate.getSelectedItem().toString();
            
            if(Database.saveData("INSERT INTO tbl_client VALUES (default, '"+CustomerNameField.getText()+"' , '"+EmailField.getText()+"' , '"+DateField.getText()+"' , '"+Birthday+"' ,'"+PhoneField.getText()+"' ,'"+SelectHowtoKnow.getSelectedItem().toString()+"' ,'"+AddressField.getText()+"' , '"+pfm.format(new Date())+"' )")){
                JOptionPane.showMessageDialog(rootPane, "Successfully register client.");
                System.out.println("Successfully register client.");
                LoadClientData();
                
                    CustomerNameField.setText("");
                    EmailField.setText("");
                    PhoneField.setText("");
                    AddressField.setText("");
                    
                    GetToday();
                
            }else{
                JOptionPane.showMessageDialog(rootPane, "Error added, please try again.");
            }
            
        }
        
    }//GEN-LAST:event_RegisterClientBtnActionPerformed

    private void ClientsTblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ClientsTblMouseClicked
        
        DefaultTableModel model = (DefaultTableModel)ClientsTbl.getModel();
        int clienttblindex = ClientsTbl.getSelectedRow();
        ClientidField.setText(model.getValueAt(clienttblindex, 0).toString());
        CustomerNameField.setText(model.getValueAt(clienttblindex, 1).toString());
        EmailField.setText(model.getValueAt(clienttblindex, 2).toString());
//        CostPrice.setText(model.getValueAt(clienttblindex, 3).toString());
        PhoneField.setText(model.getValueAt(clienttblindex, 4).toString());
        DateField.setText(model.getValueAt(clienttblindex, 5).toString());
        AddressField.setText(model.getValueAt(clienttblindex, 6).toString());
        SelectHowtoKnow.setSelectedItem(model.getValueAt(clienttblindex, 7).toString());
        
        String GetBirthday = model.getValueAt(clienttblindex, 3).toString();
        
        if(" ".equals(GetBirthday)){
            
        } else {
            
            String arr = GetBirthday;
            String[] split = arr.split(" ");
//            System.out.println("Name = " + split[0]);
//            System.out.println("Password = " + split[1]);
            SelectBirthMonth.setSelectedItem(split[0]);
            SelectBirthDate.setSelectedItem(split[1]);
            
        }
        
        
        
        
        
    }//GEN-LAST:event_ClientsTblMouseClicked

