    private void GetToday(){
        
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("dd MMMM yyyy");
        LocalDateTime now = LocalDateTime.now();
        
        DateTimeFormatter rd = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        
        NowDateLbl.setText(dtf.format(now));
        DateField.setText(rd.format(now));
    }
    
    public void LoadClientData(){
        ResultSet cd = Database.getData("SELECT * FROM tbl_client ORDER BY client_id DESC ");
        dtm.setRowCount(0);
        try {
            while(cd.next()){
                
                Vector v = new Vector();
                v.add(cd.getInt(1));
                v.add(cd.getString(2));
                v.add(cd.getString(3));
                v.add(cd.getString(5));
                
                v.add(cd.getString(6));
                v.add(cd.getString(4));
                
                v.add(cd.getString(8));
                v.add(cd.getString(7));
                
                dtm.addRow(v);
            }
        }catch(Exception ex){
            ex.printStackTrace();
        }
    }
    
